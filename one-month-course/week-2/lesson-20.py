def greet(name):
    return f"Hey {name}!"


def concatenate(word_one, word_two):
    return word_one + word_two


def age_in_dog_years(age):
    return age * 7


print(greet("emanoel"))
print(greet("ana"))

my_name = "emanoel"
print(greet(my_name))

print(concatenate("emanoel", "campos"))

print(age_in_dog_years(27))
